// Setup type definitions for built-in Supabase Runtime APIs
import "jsr:@supabase/functions-js/edge-runtime.d.ts";
import { serve } from "https://deno.land/std@0.168.0/http/server.ts";
import Stripe from "https://esm.sh/stripe@14.0.0?target=deno";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers":
    "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response("ok", { headers: corsHeaders });
  }

  try {
    const { email, name, phone, company, userId, successUrl, cancelUrl } =
      await req.json();

    // Initialize Stripe
    const stripe = new Stripe(Deno.env.get("STRIPE_SECRET_KEY") || "", {
      apiVersion: "2023-10-16",
    });

    // Create checkout session with pre-filled data
    const session = await stripe.checkout.sessions.create({
      customer_email: email,
      line_items: [
        {
          price_data: {
            currency: "usd",
            product_data: {
              name: "Gnymble Onboarding Package",
              description:
                "Personal consultation, platform setup, SMS automation strategy, and 30-day support",
            },
            unit_amount: 17900, // $179.00 in cents
          },
          quantity: 1,
        },
      ],
      mode: "payment",
      success_url:
        successUrl || `${req.headers.get("origin")}/dashboard?payment=success`,
      cancel_url:
        cancelUrl || `${req.headers.get("origin")}/dashboard?payment=cancelled`,
      metadata: {
        user_id: userId,
        email: email,
        name: name,
        phone: phone,
        company: company,
        platform: "gnymble",
      },
      billing_address_collection: "required",
      phone_number_collection: {
        enabled: true,
      },
      // Pre-fill customer information
      customer_creation: "always",
      payment_method_collection: "always",
    });

    return new Response(JSON.stringify({ url: session.url }), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
      status: 200,
    });
  } catch (error) {
    console.error("Error creating checkout session:", error);
    return new Response(JSON.stringify({ error: error.message }), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
      status: 500,
    });
  }
});

